package com.xoriant.springboard.training.java;

public class Class1 {
	private int privateVarClass1;
	int defaultVarClass1;
	protected int protectedVarClass1;
	public int publicVarClass1;
	public static int staticVarClass1;
	
	public Class1() {
		System.out.println("Constructor Class1");
	}

	private void privateMethodClass1() {
		System.out.println("Private method Class1");
	}

	void defaultMethodClass1() {
		System.out.println("Default method Class1");
	}

	protected void protectedMethodClass1() {
		System.out.println("Protected method Class1");
	}

	public void publicMethodClass1() {
		System.out.println("Public method Class1");
	}
	
	public static void staticMethodClass1() {
		System.out.println("Static method Class1");
	}
}
